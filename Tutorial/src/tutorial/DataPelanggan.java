     private void tampilkan_data1(){
        
        DefaultTableModel model1 = new DefaultTableModel();
        model1.addColumn("No.");
        model1.addColumn("Nama_Pelanggan");
        model1.addColumn("Alamat");
        model1.addColumn("No.Telepon");
       
        
        try{
        int no = 1;
        String sql = "Select * FROM datapelanggan";
        java.sql.Connection conn = (Connection)koneksi.configDB();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);
        
        while(res.next()){
            model1.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3)});
        }
        tabelpelanggan.setModel(model1);
        
    }catch (SQLException e){
        System.out.println("Error : "+e.getMessage());
    }
  }

