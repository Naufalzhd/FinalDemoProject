    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
            try{
            String sql = "DELETE FROM datapelanggan WHERE nama_pelanggan= '"+pelanggan.getText()+"'";
            java.sql.Connection conn = (Connection)koneksi.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Edit Data Berhasil..");
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        tampilkan_data1();
        kosongkan_form1();
    }//GEN-LAST:event_hapusActionPerformed

    private void inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputActionPerformed
        try {
            String sql = "INSERT INTO datapelanggan VALUES('"+pelanggan.getText()+"','"+alamat.getText()+"','"+notelepon.getText()+"')";
            java.sql.Connection conn = (Connection)koneksi.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Proses Simpan Data Berhasil..");
            tampilkan_data1();
            kosongkan_form1();

        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
}

    }//GEN-LAST:event_inputActionPerformed

    private void tabelpelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpelangganMouseClicked
int baris = tabelpelanggan.rowAtPoint(evt.getPoint());
        String nama = tabelpelanggan.getValueAt(baris, 1).toString();
        pelanggan.setText(nama);

        String Alamat = tabelpelanggan.getValueAt(baris, 2).toString();
        alamat.setText(Alamat);

        String NoTelepon = tabelpelanggan.getValueAt(baris, 3).toString();
        notelepon.setText(NoTelepon);

    }//GEN-LAST:event_tabelpelangganMouseClicked

